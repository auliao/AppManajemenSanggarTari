package zhafran.putra.appproject2ckel3

import android.content.Intent
import android.os.Bundle
import androidx.activity.enableEdgeToEdge
import androidx.appcompat.app.AppCompatActivity
import androidx.core.view.ViewCompat
import androidx.core.view.WindowInsetsCompat
import com.google.android.material.floatingactionbutton.FloatingActionButton

class LombaActivity : AppCompatActivity() {
    private lateinit var fab : FloatingActionButton
    private lateinit var recyclerView: androidx.recyclerview.widget.RecyclerView

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_lomba)
        recyclerView = findViewById(R.id.reycler_view)
        fab = findViewById(R.id.fab)

        fab.setOnClickListener {
            val intent = Intent(this, EditorLombaActivity::class.java)
            startActivity(intent)
        }
    }
}